6 # Everything in Ruby is an Object

# Everything in Ruby is an object therefore Ruby has certain built in abilities called methods
# Methods are like "skills" that certain objects have

# The interpreter is the program that takes the code you write and runs it 
# You type the code in the editor, the interpreter reads your code and it shows you the result of running your code in the console

7 # The '.length' method

# Methods are summoned using a '.'
# .length method will return the number of characters of whatever you call it upon

"I love espresso".length
==> 15

8 # The '.reverse' method

# This method will output a backwards version of the string you gave

"Eric".reverse
==> "cirE"

# Reversing input can be useful if you want to sort a list of values from highest to lowers instead of lowest to highest

